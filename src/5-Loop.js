// For Loop = Do a Loop for a certain amount of times
let dataToLoop = [3, 4, 6];
for (let index = 0; index < dataToLoop.length; index++) {
  const element = dataToLoop[index];
  console.log(element * 100);
}

let current = 0;
let count = 10;

// While loop - While something is true do something
while (current < count) {
  console.log(current);
  // Adds a single number to variable
  current += 1;
}
